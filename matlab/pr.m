% 岁差矩阵计算
% 输入TDT为简约儒略日表示的地球动力学时
function PR = pr(TDT)
dt = (TDT-mjd2000)/36525.0;
% 206264.806247096 = 3600*DEG
u1 = (2306.2181*dt + 0.30188*dt^2 + 0.017998*dt^3 )/206264.806247096;
u2 = (2004.3109*dt - 0.42665*dt^2 - 0.041833*dt^3 )/206264.806247096;
u3 = (2306.2181*dt + 1.09468*dt^2 + 0.018203*dt^3 )/206264.806247096;
PR = RotationZ(-u1)*RotationY(u2)*RotationZ(-u3);